    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if (id == R.id.nav_profile) {
            Intent profileIntent = new Intent(Home.this, Profile.class);
            startActivity(profileIntent);
        } else if (id == R.id.nav_marcBot) {

        } else if (id == R.id.nav_therapy) {

        } else if (id == R.id.nav_physician) {

        } else if (id == R.id.nav_callSomeone) {

        } else if (id == R.id.nav_settings) {

            Fragment_Settings fragment_settings = new Fragment_Settings();
            FragmentManager fragmentManager = getSupportFragmentManager();
            fragmentManager.beginTransaction().replace(R.id.mainLayout, fragment_settings).commit();
        } else if (id == R.id.nav_signOut) {
            mAuth.signOut();
            sendUserToLogInActivity();
        }

        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }

    private void sendUserToLogInActivity() {
        Intent logInIntent = new Intent(Home.this, MainActivity.class);
        logInIntent.addFlags(Intent.FLAG_ACTIVITY_NEW_TASK | Intent.FLAG_ACTIVITY_CLEAR_TASK);
        startActivity(logInIntent);
        finish();
    }
